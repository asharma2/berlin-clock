package com.aks.clock.model;

/**
 * The class will hold the user input for each row
 * 
 * @author atul
 *
 */
public class Input {

	int one;
	int two;
	int three;
	int four;

	public int getOne() {
		return one;
	}

	public void setOne(int one) {
		this.one = one;
	}

	public int getTwo() {
		return two;
	}

	public void setTwo(int two) {
		this.two = two;
	}

	public int getThree() {
		return three;
	}

	public void setThree(int three) {
		this.three = three;
	}

	public int getFour() {
		return four;
	}

	public void setFour(int four) {
		this.four = four;
	}

}
